run_gate_ctest_check() {
  local regex="$1"
  local label="$2"
  local varname="$3"
  if ! command -v ctest >/dev/null 2>&1; then
    echo "[EDITOR-GATE] WARN ctest not found; skipping $label"
    eval "$varname=\"SKIPPED\""
    return
  fi
  echo "[EDITOR-GATE] CTest $label"
  local list
  list=$(ctest --test-dir "$ASSEMBLY_ROUNDTRIP_CTEST_BUILD_DIR" -N -R "$regex" 2>/dev/null || true)
  if echo "$list" | grep -q "Total Tests: 0"; then
    eval "$varname=\"MISSING\""
    echo "[EDITOR-GATE] WARN missing CTest $label"
    return
  fi
  if ctest --test-dir "$ASSEMBLY_ROUNDTRIP_CTEST_BUILD_DIR" -R "$regex" -V; then
    eval "$varname=\"PASS\""
    echo "[EDITOR-GATE] OK CTest $label"
  else
    eval "$varname=\"FAIL\""
    echo "[EDITOR-GATE] FAIL CTest $label"
  fi
}


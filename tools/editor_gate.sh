count_case_items() {
  local path="$1"
  python3 - "$path" <<'PY'
import json
import sys
path = sys.argv[1]
if not path:
  print(0)
  raise SystemExit(0)
try:
  payload = json.load(open(path, "r", encoding="utf-8"))
except Exception:
  print(0)
  raise SystemExit(0)
print(len(payload) if isinstance(payload, list) else 0)
PY
}


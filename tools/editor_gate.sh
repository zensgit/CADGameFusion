allocate_ui_flow_port() {
  if [[ -n "$EDITOR_UI_FLOW_PORT" ]]; then
    UI_FLOW_PORT_ALLOCATOR_STATUS="SKIPPED_EXPLICIT"
    UI_FLOW_PORT_ALLOCATOR_DETAIL="explicit:$EDITOR_UI_FLOW_PORT"
    UI_FLOW_PORT_ALLOCATOR_RC=0
    return 0
  fi

  local output rc
  set +e
  output="$(python3 - <<'PY' 2>&1
import socket

s = socket.socket()
try:
  s.bind(("127.0.0.1", 0))
except Exception as exc:
  print(f"{type(exc).__name__}: {exc}")
  raise SystemExit(1)

print(s.getsockname()[1])
s.close()
PY
)"
  rc=$?
  set -e

  output="$(printf "%s" "$output" | tr '\r\n' ' ' | sed -E 's/[[:space:]]+/ /g; s/^ //; s/ $//')"
  if [[ "$rc" -eq 0 && "$output" =~ ^[0-9]+$ ]]; then
    EDITOR_UI_FLOW_PORT="$output"
    UI_FLOW_PORT_ALLOCATOR_STATUS="OK"
    UI_FLOW_PORT_ALLOCATOR_DETAIL="auto:$EDITOR_UI_FLOW_PORT"
    UI_FLOW_PORT_ALLOCATOR_RC=0
    return 0
  fi

  UI_FLOW_PORT_ALLOCATOR_STATUS="FAILED"
  UI_FLOW_PORT_ALLOCATOR_DETAIL="${output:-allocator_rc_${rc}}"
  UI_FLOW_PORT_ALLOCATOR_RC="$rc"
  return 1
}


  else
    EDITOR_SMOKE_GENERATED_COUNT_ACTUAL="0"
    if [[ "$EDITOR_SMOKE_GENERATED_COUNT_DECLARED" -ne 0 ]]; then
      EDITOR_SMOKE_GENERATED_COUNT_MISMATCH="1"
      echo "[EDITOR-GATE] WARN generated case file missing for declared count=$EDITOR_SMOKE_GENERATED_COUNT_DECLARED path=$EDITOR_SMOKE_GENERATED_CASES_PATH"
    fi
  fi
fi

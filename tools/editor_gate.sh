    if ! [[ "$EDITOR_SMOKE_GENERATED_COUNT_ACTUAL" =~ ^[0-9]+$ ]]; then
      EDITOR_SMOKE_GENERATED_COUNT_ACTUAL="0"
    fi
    if [[ "$EDITOR_SMOKE_GENERATED_COUNT_ACTUAL" -ne "$EDITOR_SMOKE_GENERATED_COUNT_DECLARED" ]]; then
      EDITOR_SMOKE_GENERATED_COUNT_MISMATCH="1"
      echo "[EDITOR-GATE] WARN generated case count mismatch: declared=$EDITOR_SMOKE_GENERATED_COUNT_DECLARED actual=$EDITOR_SMOKE_GENERATED_COUNT_ACTUAL path=$EDITOR_SMOKE_GENERATED_CASES_PATH"

    local regex="$1"
    local label="$2"
    local varname="$3"
    if command -v ctest >/dev/null 2>&1; then
      echo "[LOCAL-CI] CTest $label check"
      local list
      list=$(ctest --test-dir "$BUILD_DIR" -N -R "$regex" 2>/dev/null || true)

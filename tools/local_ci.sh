        FAIL_REASONS+=("ctestAssemblyRoundtripFirstFailedCase=$CTEST_ASSEMBLY_ROUNDTRIP_FIRST_FAILED_CASE")
      fi
    fi

    if [[ $FAIL_FLAG -ne 0 ]]; then
      echo "[LOCAL-CI] =============================================="
      echo "[LOCAL-CI] STRICT_EXIT failure triggered!"
